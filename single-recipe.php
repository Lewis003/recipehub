            <h5>Githeri</h5>
            <p>Prep : 15min | Cook : 50min
              The beauty of Githeri is that you can prepare it using any type of beans. Githeri can be eaten right after boiling, but most Kenyans fry it and add potatoes, vegetables, and spices to make it even more delicious.
              Ingredients Vegetable oil 1 medium-sized onion to be chopped 1 teaspoon of smoked paprika 1/4 teaspoon of chilli ground 1 and Â½ cup of beans 1 cup of maize 2 large chopped tomatoes Salt to taste Coriander
              Preparation method Boil the maize and beans in one pot until they are soft and edible. Drain all the water in the pan and set the maize and beans mixture aside. Heat the oil in a sufuria under medium heat. Fry the onions in the oil until they are golden brown. Add your chopped tomatoes and continue stirring. Add the boiled beans and maize mixture and stir well. Pour in the paprika while stirring for another minute to prevent burning. Add 1 cup of water and salt, then cover the sufuria and allow to cook for about 20 minutes. Sprinkle your chopped coriander leaves, mix well, and serve.
